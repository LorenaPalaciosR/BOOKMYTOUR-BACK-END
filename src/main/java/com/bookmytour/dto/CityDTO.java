package com.bookmytour.dto;

public class CityDTO {
    private Integer cityId;
    private String name;

    // Getters y Setters
    public Integer getCityId() {
        return cityId;
    }

    public void setCityId(Integer cityId) {
        this.cityId = cityId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
